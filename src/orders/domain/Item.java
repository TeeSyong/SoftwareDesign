package orders.domain;

public class Item {
	private int itemNo;
	private String itemType;
	private String itemName;
	private double itemPrice;
	
	public int getItemNo() {
		return itemNo;
	}
	
	public String getItemType()
	{
		return itemType;
	}
	
	public String getItemName() {
		return itemName;
	}
	
	public double itemPrice() {
		return itemPrice;
	}
}
